#include "EE3420.INC"
	ORG $1000
VAR1 DC.W 0 ;DEFINES ONE WORD INITIALIZED TO 0
VAR2 DC.W 10 ;DEFINES ONE WORD INITIALIZED TO 10
SUM DS.B 1 ;DEFINES ONE WORD INITIALIZED TO 0
ODDPARITY DS.B 1 ;DEFINES ONE WORD INITIALIZED TO 0
ARRAY DC.B 1,0,1,0,1,1,1,0,0,0 ;DEFINES BIT ARRAY OF TEN NUMBERS

	ORG $2000
MAIN: ;THIS WILL BE THE FIRST LINE OF CODE EXECUTED
	LDX #0 ;INDEX X = 0
	LDAA #0 ;ACCUMULATOR A = 0
	LDY VAR2 ;INDEX Y = VALUE FROM MEMORY LOCATION VAR2
LOOP1:
	ADDA ARRAY,X ; Add Array[X] to A
	INX ;INCREMENT INDEX X
	DBNE Y,LOOP1 ;DECREMENT Y AND BRANCH TO LOOP1 IF RESULT
		;IS NOT EQUAL TO ZERO
	STAA SUM
	CLC	;Clear C Flag
	LSRA	;Shift to the Right 
		;C now is 1 if the LSB of A is 1
		;if A is 1 then oddparity is 1
	BLO ODD
	BHS EVENP

ODD:
	MOVB #0, ODDPARITY
	JMP ENDP
EVENP:
	MOVB #1, ODDPARITY
	JMP ENDP
ENDP:	

	RTS ;END OF "MAIN" FUNCTION